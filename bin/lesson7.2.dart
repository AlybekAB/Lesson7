  var num = 2;
  switch (num) {
    case 1:
      {
        var result = "Zima";
        print(result);
        break;
      }
    case 2:
      {
        var result = "Leto";
        print(result);
        break;
      }
    case 3:
      {
        var result = "Vesna";
        print(result);
        break;
      }
    case 4:
      {
        var result = "Osen";
        print(result);
        break;
      }
    default:
      print("Out of range");
      break;
  }
}
